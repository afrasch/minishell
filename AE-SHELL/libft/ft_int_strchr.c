#include "libft.h"

int	ft_int_strchr(char *s, int c)
{
	int		i;

	i = 0;
	if (s == NULL)
		return (-1);
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
		{
			return (i);
		}
		i++;
	}
	if (s[i] == '\0' && c == '\0')
	{
		return (i);
	}
	return (-1);
}
