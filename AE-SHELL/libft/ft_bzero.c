#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	int		j;
	char	*p;

	i = 0;
	p = s;
	j = n;
	while (i < j)
	{
		p[i] = '\0';
		i++;
	}
}
