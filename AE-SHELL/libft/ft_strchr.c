char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*p;

	p = (char *)s;
	i = 0;
	while (p[i] != '\0')
	{
		if (p[i] == (char)c)
		{
			return (p + i);
		}
		i++;
	}
	if (s[i] == '\0' && c == '\0')
	{
		return (p + i);
	}
	return (0);
}
